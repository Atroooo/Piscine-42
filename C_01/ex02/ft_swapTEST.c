#include <stdio.h>

void	ft_swap(int *a, int *b)
{
	int temp;
	temp = *a;
	*a = *b;
	*b = temp;
	printf("%d %d", *a, *b);

}

int main()
{
	int a = 1, b = 3;
	ft_swap(&a, &b);
}
